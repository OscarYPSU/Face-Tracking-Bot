const unsigned char* frames[]{
	epd_bitmap_New_Piskel_1, epd_bitmap_New_Piskel_2, epd_bitmap_New_Piskel_3
};
const int frameCount = sizeof(frames) / sizeof(frames[0]);
unsigned long lastUpdatedMilis = 0; // so delay doesnt actually delay the prcoess
const int delayInterval = 100;
int framesIndex = 0;
void studyLoop(){
	unsigned long currentMilis = millis();
	if (currentMilis - lastUpdatedMilis >= delayInterval){
		display.clearDisplay();
		display.drawBitmap(0, 0, frames[framesIndex], 128, 64, SSD1306_WHITE);
		display.display();
		framesIndex++;
		if (framesIndex >= frameCount){
			framesIndex = 0;
		}
		lastUpdatedMilis = currentMilis;
	} 
}






