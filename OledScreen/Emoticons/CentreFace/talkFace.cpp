const unsigned char* framesTalkingFace[]{
	talkFaceFrame1, talkFaceFrame2, talkFaceFrame2
};
const int frameCountTalkingFace = sizeof(framesTalkingFace) / sizeof(framesTalkingFace[0]);
unsigned long lastUpdatedTalkFaceMilis = 0; // so delay doesnt actually delay the prcoess
const int delayTalkFaceInterval = 250;
int framesIndexTalkingFace = 0;
void talkFaceLoop(){
	currentMilis = millis();
	if (currentMilis - lastUpdatedTalkFaceMilis >= delayTalkFaceInterval){
		display.clearDisplay();
		display.drawBitmap(0, 0, framesTalkingFace[framesIndexTalkingFace], 128, 64, SSD1306_WHITE);
		display.display();
		framesIndexTalkingFace++;
		if (framesIndexTalkingFace >= frameCountTalkingFace){
			framesIndexTalkingFace = 0;
		}
		lastUpdatedTalkFaceMilis = currentMilis;
	} 
}